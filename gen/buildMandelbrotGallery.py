def generateMandelbrotImageHtml(*, SITE_ROOT_REL, IMG_DATE, IMG_THUMB, IMG_TITLE, IMG_DESC, IMG_PARAMS, IMG_RENDER_URLS_BY_KEY, IMG_RE, IMG_IM, IMG_MAG, IMG_SCALE, IMG_ITER):
	RENDERS_HTML = ""

	# create html list of renders
	for render_key in sorted(list(IMG_RENDER_URLS_BY_KEY.keys())):
		RENDER_URL = IMG_RENDER_URLS_BY_KEY[render_key]
		RENDER_NAME = RENDER_URL.split('/')[-1]
		RENDERS_HTML += f'\n<li><a target="_blank" href="{RENDER_URL}">{RENDER_NAME}</a></li>'

	if len(IMG_RENDER_URLS_BY_KEY) > 0:
		RENDERS_HTML = "<p>Available renders:</p>\n<ul>" + RENDERS_HTML + "\n</ul>"

	return f"""
<details class="width-resp-50-100">
	<summary>
		<img class="width-100" src="{IMG_THUMB}"/>
	</summary>
	<p>{IMG_DATE}: {IMG_TITLE}</p>
	<p>{IMG_DESC}</p>
	<p>Click
		<a target="_blank" href="{SITE_ROOT_REL}/very-plotter/{IMG_PARAMS}">
		here</a> to view this location in Very Plotter.</p>
	{RENDERS_HTML}
	<p id="loc">Location:<br/>
	Re:&nbsp;{IMG_RE}<br/>
	Im:&nbsp;{IMG_IM}<br/>
	Magnification:&nbsp;{IMG_MAG} <small>(where 1.0 fits entire Mandelbrot set into the window)</small><br/>
	Scale:&nbsp;{IMG_SCALE} <small>(pixels/unit, for renders seen here)</small><br/>
	Iterations:&nbsp;{IMG_ITER}</p>
</details>"""
